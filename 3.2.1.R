library(ggplot2)
a <- c(11.900220, 7.583132, 7.915741, 9.804360, 11.091254, 11.222213, 12.575811, 10.132034, 8.181249, 16.389524)
b <- c(15.07913, 17.431747, 9.864124, 13.390633, 20.030938, 12.663809, 11.571601, 16.09156, 14.555116, 15.278246)
# シャピロ・ウィルク検定(与えられた標本が正規分布かどうかを判定する)
qplot(a, geom="histogram", binwidth=0.5)
qplot(b, geom="histogram", binwidth=0.5)
shapiro.test(a)
shapiro.test(b)
# F検定で２つの標本の分散に違いがないことを確認する
var.test(a, b)
t.test(a, b, var.equal=TRUE)
